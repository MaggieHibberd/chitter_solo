CREATE DATABASE "chitter_management";

CREATE TABLE users(id SERIAL PRIMARY KEY, email VARCHAR(60), password VARCHAR(100));

CREATE TABLE peeps(id SERIAL PRIMARY KEY, content VARCHAR(280), timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP, user_id INTEGER REFERENCES users (id));

